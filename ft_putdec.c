#include "ft_printf.h"

int	ft_putdec(int nbr)
{
	int	i;

	i = 0;
	if (nbr < 0)
		i += write(1, "-", 1);
	return (ft_putnbr((unsigned long long)nbr, 10, "0123456789") + i);
}
